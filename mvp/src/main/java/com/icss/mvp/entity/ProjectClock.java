package com.icss.mvp.entity;

import java.util.Date;

public class ProjectClock {
	
	private String company;
	private String area;
	private String proline;
	private String subproline;
	private String pdu;
	private String author;
	private String name;
	private Date worktime;
	private Date offtime;
	private String type;
	private String idnumber;
	private String po;
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getProline() {
		return proline;
	}
	public void setProline(String proline) {
		this.proline = proline;
	}
	public String getSubproline() {
		return subproline;
	}
	public void setSubproline(String subproline) {
		this.subproline = subproline;
	}
	public String getPdu() {
		return pdu;
	}
	public void setPdu(String pdu) {
		this.pdu = pdu;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getWorktime() {
		return worktime;
	}
	public void setWorktime(Date worktime) {
		this.worktime = worktime;
	}
	public Date getOfftime() {
		return offtime;
	}
	public void setOfftime(Date offtime) {
		this.offtime = offtime;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getIdnumber() {
		return idnumber;
	}
	public void setIdnumber(String idnumber) {
		this.idnumber = idnumber;
	}
	public String getPo() {
		return po;
	}
	public void setPo(String po) {
		this.po = po;
	}
}
